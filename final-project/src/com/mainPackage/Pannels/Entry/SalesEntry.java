    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        // TODO add your handling code here:
        Object OItem = txtItem.getSelectedItem();
        sItem = OItem.toString();
        sQty = txtQuantity.getText();
        sSellingPrice = txtSellingPrice.getText();
        sFactoryPrice = txtFactoryPrice.getText();
        sAmount = txtAmount.getText();

        if (sItem.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Select Items");
        } else if (sQty.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Quantity is Empty !!");
        } else if (!sQty.matches("[0-9_]+")) {
            JOptionPane.showMessageDialog(null, "Quantity not valid !! Enter digits");
        } else if (!itemstock.ItemStockCheck(sItem, new BigDecimal(sQty))) {
            JOptionPane.showMessageDialog(null, "Quantity is Greater then Stock");
        } else if (sFactoryPrice.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Factory Price is Empty !!");
        } else if (!sFactoryPrice.matches("[0-9_]+")) {
            JOptionPane.showMessageDialog(null, "Factory Price not valid !! Enter digits");
        } else if (sSellingPrice.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selling Price is Empty !!");
        } else if (!sSellingPrice.matches("[0-9_]+")) {
            JOptionPane.showMessageDialog(null, "Selling Price not valid !! Enter digits");
        } else if (sAmount.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Click Total button Amount is Empty !!");
        } else if (!sAmount.matches("[0-9_]+")) {
            JOptionPane.showMessageDialog(null, "Amount Price not valid !! Enter digits");
        } else {
            //Integer
            nQty = Integer.parseInt(sQty);
            BigDecimal bg_qty = new BigDecimal(nQty);
            nSellingPrice = Integer.parseInt(sSellingPrice);
            BigDecimal bg_sp = new BigDecimal(nSellingPrice);

            nFactoryPrice = Integer.parseInt(sFactoryPrice);
//            int c = nQty * nSellingPrice;
            BigDecimal c = bg_qty.multiply(bg_sp);
            String fc = c.toString();

            int profit = nSellingPrice - nFactoryPrice;
            int fprofit = profit * nQty;
            BigDecimal bgfprofit = new BigDecimal(fprofit);
            String sbfprofit = bgfprofit.toString();

            DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
            Object[] add = new Object[6];
            int i = 1;
            add[0] = 1 + tblProduct.getRowCount();
            add[1] = sItem;
            add[2] = sQty;
            add[3] = sSellingPrice;
            add[4] = fc;
            add[5] = sbfprofit;
            model.addRow(add);

            txtTotal.setText(Integer.toString(getSum()));
            txtAmount.setText("");
        }


    }//GEN-LAST:event_btnAddItemActionPerformed

