    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:

        try {
            txtNetAmount.setText(getNetAmount().setScale(2, RoundingMode.DOWN).toString());
        } catch (NumberFormatException e) {
//            JOptionPane.showMessageDialog(null, "Enter vat");
        }

        sDate = txtDate.getText();
        sBillno = txtBillno.getText();
        Object Oclintname = txtCompanyName.getSelectedItem();
        sClientName = Oclintname.toString();
        sPhone = txtPhone.getText();
        sAddress = txtAddress.getText();
        sTotal = txtTotal.getText();
        sVat = txtVat.getText();
        sNetAmount = txtNetAmount.getText();
        sQty = txtQuantity.getText();

        if (sDate.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Date is Empty !!");
        } else if (!sDate.matches("[0-9/_]+")) {
            JOptionPane.showMessageDialog(null, "Date Invalid !!");
        } else if (!sDate.matches("\\d{4}/\\d{1,2}/\\d{1,2}")) {
            JOptionPane.showMessageDialog(null, "Date not valid date should be YYYY/MM/DD");
        } else if (sBillno.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Bill no is Empty !!");
        } else if (!sBillno.matches("[0-9_]+")) {
            JOptionPane.showMessageDialog(null, "Bill no Invalid !! Enter Digits");
        } else if (sClientName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Clint Name is Empty !!");
        } else if (sAddress.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Address is Empty !!");
        } else if (sPhone.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Phone no. is Empty !!");
        } else if (!sPhone.matches("[0-9_]+")) {
            JOptionPane.showMessageDialog(null, "Phone no not valid !!");
        } else if (sTotal.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Amount is empty, First entry Item details !!");
        } else if (sVat.isEmpty()) {
            JOptionPane.showMessageDialog(null, "VAT is empty !!");
        } else if (!sVat.matches("[0-9._]+")) {
            JOptionPane.showMessageDialog(null, "VAT Not Valid !!");
        } else if (sNetAmount.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Net Amount is empty, Click Calculate Net Amount buttom !!");
        } else {
            if (JOptionPane.showConfirmDialog(null, "   Sales Details   " + "\n Date :- " + sDate + "\n Bill no :- " + sBillno + "\n Client Name :- " + sClientName + "\n Address :- " + sAddress + "\n Total Amount :- " + sTotal + "\n VAT (%)  " + sVat + "\n Net Amount :- " + sNetAmount, "Title", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                int rows = tblProduct.getRowCount();

                for (int row = 0; row < rows; row++) {
                    sItem = (String) tblProduct.getValueAt(row, 1);
                    sQty = (String) tblProduct.getValueAt(row, 2);
                    int nQty1 = Integer.parseInt(sQty);
                    sSellingPrice = (String) tblProduct.getValueAt(row, 3);
                    bg_sellingPrice = new BigDecimal(sSellingPrice);
                    sTTotal = (String) tblProduct.getValueAt(row, 4);
                    bg_total = new BigDecimal(sTTotal);
                    sProfit = (String) tblProduct.getValueAt(row, 5);
                    bg_profit = new BigDecimal(sProfit);

                    //Sales Item Details
                    itSalemodel.setDate(getnDate());
                    itSalemodel.setBillno(getnBill());
                    itSalemodel.setClientname(sClientName);
                    itSalemodel.setItem(sItem);
                    itSalemodel.setItemprice(bg_sellingPrice);
                    itSalemodel.setQuantity(nQty1);
                    itSalemodel.setTotalprice(bg_total);
                    itSalemodel.setProfit(bg_profit);
                    itSaleDao.insert(itSalemodel);

                    //to Decrease copy stock
                    itemstockmodel.setItemname(sItem);
                    itemstockmodel.setQuantity(new BigDecimal(sQty));
                    itemstock.itemDecrease(itemstockmodel);
                    
                    //insert item details
              itemdetailmodel.setDate(getnDate());
              itemdetailmodel.setItemname(sItem);
              itemdetailmodel.setIncrement(new BigDecimal(0));
              itemdetailmodel.setDecrement(new BigDecimal(sQty));
              itemdetaildao.insert(itemdetailmodel);

                }
                salemodel.setDate(getnDate());
                salemodel.setBillno(getnBill());
                salemodel.setClientname(sClientName);
                salemodel.setAddress(sAddress);
                salemodel.setPhone(sPhone);
                salemodel.setTotalamt(new BigDecimal(sTotal));
                salemodel.setVat(new BigDecimal(sVat));
                salemodel.setNetamt(new BigDecimal(sNetAmount));
                saledao.insert(salemodel);

                //Sales Receipt for ledger
                receiptmodel.setDate(getnDate());
                receiptmodel.setClientname(sClientName);
                receiptmodel.setParticular("Sales Account");
                receiptmodel.setVchtype("Bill No");
                receiptmodel.setVchno(getnBill());
                receiptmodel.setDebit(new BigDecimal(sNetAmount));
                receiptmodel.setCredit(new BigDecimal("0"));
                receiptdao.insert(receiptmodel);

                MainProgram.mainframe.mainPane.remove(this);
                MainProgram.mainframe.mainPane.add(new SalesEntry());
                SwingUtilities.updateComponentTreeUI(MainProgram.mainframe);

                JOptionPane.showMessageDialog(null, "Record Insert");
            } else {
                JOptionPane.showMessageDialog(null, "Record not saved !!!");
            }

        }


    }//GEN-LAST:event_btnSubmitActionPerformed

