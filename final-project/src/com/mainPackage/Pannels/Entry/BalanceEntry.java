    private void txtDepositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDepositActionPerformed
        // TODO add your handling code here:
        String sDate = txtDate.getText();
        Object oBankName = txtBankName.getSelectedItem();
        String sBankName = oBankName.toString();
        String sAmount = txtBankAmount.getText();
        
        if(sDate.isEmpty()){
            JOptionPane.showMessageDialog(null, "Date is empty !!");
        }else if(!sDate.matches("[0-9/_]+")){
            JOptionPane.showMessageDialog(null, "Date not valid !!");
        } else if(!sDate.matches("\\d{4}/\\d{1,2}/\\d{1,2}")){
            JOptionPane.showMessageDialog(null, "Date not valid date should be YYYY/MM/DD");
        }
        else if(sBankName.isEmpty()){
            JOptionPane.showMessageDialog(null, "Bank name is empty !!");
        }
        else if(sAmount.isEmpty()){
            JOptionPane.showMessageDialog(null, "Amount is empty !!");
        }else if(!sAmount.matches("[0-9_]+")){
            JOptionPane.showMessageDialog(null, "Amount not valid !!");
        }
        else{
         if(JOptionPane.showConfirmDialog(null, "Are you Sure want to Deposit"  +"\n Date :- " +sDate + "\n Bank Name  " + sBankName + "\n Amount  " +sAmount,"Deposit", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
             
        
        //Bank Ledger
        bankledgermodel.setDate(getnDate());
        bankledgermodel.setBankname(sBankName);
        bankledgermodel.setParticular("Deposit");
        bankledgermodel.setDebit(new BigDecimal(0));
        bankledgermodel.setCredit(new BigDecimal(sAmount));
        bankledgerdao.insert(bankledgermodel);
        
          //BankBalance
        bankdepmodel.setBankname(sBankName);
        bankdepmodel.setAmount(new BigDecimal(sAmount));
        bankdepositdao.bInsert(bankdepmodel);
        //Daily Book cheque
        dailybookmodel.setDate(getnDate());
        dailybookmodel.setParticular("Cheque deposit " + sBankName);
        dailybookmodel.setAmount(new BigDecimal(sAmount));
        indao.insertIncom(dailybookmodel);
       JOptionPane.showMessageDialog(null, "Amount Deposit Success"); 
       
         }else{
             JOptionPane.showMessageDialog(null, "Deposit Cancelled"); 
         }
         txtBankAmount.setText("");
         
        }
    }//GEN-LAST:event_txtDepositActionPerformed

