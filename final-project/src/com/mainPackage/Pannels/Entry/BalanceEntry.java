    private void btnCashDepositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCashDepositActionPerformed
        // TODO add your handling code here:
        sdate = txtCashDate.getText();
        String samount = txtCashAmount.getText();
        if(sdate.isEmpty()){
            JOptionPane.showMessageDialog(null, "Date is Empty !!");
        }else if(!sdate.matches("\\d{4}/\\d{1,2}/\\d{1,2}")){
            JOptionPane.showMessageDialog(null, "Date not valid date should be YYYY/MM/DD");
        }
        else if(!sdate.matches("[0-9/_]+")){
            JOptionPane.showMessageDialog(null, "Date not valid !!");
        }else  if(samount.isEmpty()){
            JOptionPane.showMessageDialog(null, "Amount is Empty !!");
        }else if(!samount.matches("[0-9_]+")){
            JOptionPane.showMessageDialog(null, "Enter digits in amount !!");
        }else{
            if(JOptionPane.showConfirmDialog(null, "Are you sure want to deposit ?" +"\n Date :-" + sdate + "\n Amount  RS :- " + samount,"Cash Deposit",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
              
               
               //cash deposit ledger
               cashledgermodel.setDate(getnCashDate());
               cashledgermodel.setParticular("Cash Deposit");
               cashledgermodel.setDebit(new BigDecimal(0));
               cashledgermodel.setCredit(new BigDecimal(samount));
                cashledgerdao.insert(cashledgermodel);
                
                //cash deposit
                 cashdepmodel.setParticular("Cash Deposit");
               cashdepmodel.setCash(new BigDecimal(samount));
               cashdepositdao.insert(cashdepmodel);
               
               //Daily book
               dailybookmodel.setDate(getnCashDate());
               dailybookmodel.setParticular("Cash Deposit");
               dailybookmodel.setAmount(new BigDecimal(samount));
               indao.insertIncom(dailybookmodel);
                JOptionPane.showMessageDialog(null, "success");
            }else{
                JOptionPane.showMessageDialog(null, "Balance not entry !!");
            }
            txtCashAmount.setText("");
        } 
    }//GEN-LAST:event_btnCashDepositActionPerformed

